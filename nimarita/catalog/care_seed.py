_QUICK_REPLY_MAP = {item.code: item for item in CARE_QUICK_REPLY_DEFINITIONS}


def get_quick_reply(code: str) -> CareQuickReplySeed | None:
    return _QUICK_REPLY_MAP.get(code)


def get_quick_reply_pages(category: str, *, page_size: int = 3, max_pages: int = 2) -> list[list[CareQuickReplySeed]]:
    specific = [item for item in CARE_QUICK_REPLY_DEFINITIONS if item.category == category]
    generic = [item for item in CARE_QUICK_REPLY_DEFINITIONS if item.category == 'generic']
    items = [*specific, *generic]
    if not items:
        items = generic[:]
    pages: list[list[CareQuickReplySeed]] = []
    for offset in range(0, min(len(items), page_size * max_pages), page_size):
        page = items[offset : offset + page_size]
        if page:
            pages.append(page)
    if not pages:
        pages.append(generic[:page_size])
    return pages
